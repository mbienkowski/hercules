"""OpenCode manifest + plugin-entry emitters.

Adapted from PR #11's ``scripts/generate_opencode.py`` (``escape_ts_string``, ``_ts_object_literal``,
``generate_plugin_js``, ``generate_opencode_json``). The one behavioural change: the plugin entry now
lives **inside** ``dist/opencode/`` (not one level up), so ``PLUGIN_ROOT = path.resolve(__dirname)``
and asset paths are relative to it, plus a startup assertion that the assets exist.
"""
from __future__ import annotations

import json
import re


def escape_ts_string(value: str) -> str:
    """Return a JSON-stringified TypeScript/JS string literal."""
    return json.dumps(value, ensure_ascii=False)


def ts_object_literal(obj, indent: int = 8) -> str:
    """Render a JSON-serialisable object as a JS object literal (keys bare when identifier-safe)."""
    spaces = " " * indent
    if isinstance(obj, dict):
        if not obj:
            return "{}"
        items = []
        for k, v in obj.items():
            key = k if re.match(r"^[a-zA-Z_][a-zA-Z0-9_]*$", k) else escape_ts_string(k)
            items.append(f"{spaces}  {key}: {ts_object_literal(v, indent + 2)},")
        return "{\n" + "\n".join(items) + f"\n{spaces}}}"
    if isinstance(obj, list):
        if not obj:
            return "[]"
        return "[" + ", ".join(ts_object_literal(v, indent + 2) for v in obj) + "]"
    if isinstance(obj, bool):
        return "true" if obj else "false"
    if isinstance(obj, str):
        return escape_ts_string(obj)
    if obj is None:
        return "null"
    return str(obj)


_PLUGIN_JS_TEMPLATE = """// Generated by scripts/build - do not edit manually.
const path = require("path");
const fs = require("fs");

// This file lives at the root of dist/opencode/; assets are its siblings.
const PLUGIN_ROOT = path.resolve(__dirname);
for (const asset of [__INSTRUCTIONS_PATH__, __SKILLS_PATH__]) {
  if (!fs.existsSync(path.join(PLUGIN_ROOT, asset))) {
    throw new Error("hercules opencode plugin: missing asset " + asset);
  }
}

module.exports = async () => {
  return {
    config: (cfg) => {
      cfg.default_agent = __DEFAULT_AGENT__;
      cfg.instructions = [
        ...(cfg.instructions || []),
        path.join(PLUGIN_ROOT, __INSTRUCTIONS_PATH__),
      ];
      cfg.skills = cfg.skills || {};
      cfg.skills.paths = [
        ...(cfg.skills.paths || []),
        path.join(PLUGIN_ROOT, __SKILLS_PATH__),
      ];
      cfg.agent = { ...(cfg.agent || {}), ...__AGENT_ENTRIES__ };
      cfg.command = { ...(cfg.command || {}), ...__COMMAND_ENTRIES__ };
    },
  };
};
"""


def generate_plugin_js(default_agent, agents, commands, instructions_path="instructions.md", skills_path="skills"):
    """Generate the CommonJS OpenCode plugin entry with inlined agents/commands.

    *agents* / *commands* are ``(name, metadata, prompt)`` triples; command keys stay ``hercules:<name>``.
    """
    agent_entries = {
        name: {"description": meta.get("description", ""), "mode": meta.get("mode", "subagent"), "prompt": prompt}
        for name, meta, prompt in agents
    }
    command_entries = {
        f"hercules:{name}": {"description": meta.get("description", ""), "agent": meta.get("agent", "hercules"), "template": prompt}
        for name, meta, prompt in commands
    }
    return (
        _PLUGIN_JS_TEMPLATE
        .replace("__DEFAULT_AGENT__", escape_ts_string(default_agent))
        .replace("__INSTRUCTIONS_PATH__", escape_ts_string(instructions_path))
        .replace("__SKILLS_PATH__", escape_ts_string(skills_path))
        .replace("__AGENT_ENTRIES__", ts_object_literal(agent_entries))
        .replace("__COMMAND_ENTRIES__", ts_object_literal(command_entries))
    )


def generate_opencode_json(default_agent="hercules", instructions="instructions.md", skills_path="skills") -> dict:
    """Build ``opencode.json`` (used when the dist/opencode tree is opened directly)."""
    return {
        "$schema": "https://opencode.ai/config.json",
        "default_agent": default_agent,
        "instructions": [instructions],
        "skills": {"paths": [skills_path]},
    }
